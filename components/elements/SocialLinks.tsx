const platformColors: Record<SocialPlatform, string> = {
  twitter: '#000000',
  instagram: '#E1306C',
  linkedin: '#0A66C2',
  github: '#181717',
  youtube: '#FF0000',
  tiktok: '#000000',
}

export default function SocialLinks({ config }: { config: SocialLinksConfig }) {
  const { heading, links, style, align } = config.data
  const sorted = [...links].sort(byOrder)

  return (
    <div>
      {heading && <p className="text-xs font-medium text-slate-400 mb-3 text-center uppercase tracking-wider">{heading}</p>}
      <div className={`flex flex-wrap gap-2 ${alignClass[align]}`}>
        {sorted.map(link =>
          style === 'buttons' ? (
            <a
              key={link.id}
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              className="relative flex items-center gap-2 pl-4 pr-3.5 py-2 rounded-xl border border-slate-200 bg-white hover:bg-slate-50 hover:-translate-y-0.5 text-slate-700 text-xs font-medium transition-all shadow-sm overflow-hidden"
            >
              <span
                className="absolute inset-y-0 left-0 w-1"
                style={{ backgroundColor: platformColors[link.platform] }}
              />
              <span style={{ color: platformColors[link.platform] }}>{icons[link.platform]}</span>
              {link.label}
            </a>
          ) : (
            <a
              key={link.id}
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              className="w-10 h-10 rounded-xl flex items-center justify-center bg-white border border-slate-200 hover:bg-slate-50 hover:-translate-y-0.5 transition-all shadow-sm"
              title={link.label}
              style={{ color: platformColors[link.platform] }}
            >
              {icons[link.platform]}
            </a>
          )
        )}
      </div>
    </div>
  )
}
